package org.tensorflow.lite.examples.detection;

public class Pokemon {

    private String name;
    private String height;
    private String weight;
    private Integer id;

    public Pokemon() {

    }

    public String getName() {
        return name;
    }

    public String getHeight() {
        return height;
    }

    public String getWeight() {
        return weight;
    }

    public Integer getId(){
        return id;
    }
}
